#include "bsq.h"

void	print_map(t_map *m)
{
	int	i;

	if (!m || !m->data)
		return ;
	i = 0;
	while (i < m->rows)
	{
		write(1, m->data + (i * m->cols), m->cols);
		write(1, "\n", 1);
		i++;
	}
}
